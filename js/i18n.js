// Set language
function setLanguage(lang) {
  localStorage.setItem('tomachef_lang', lang);
  applyTranslations(lang);
  // Update select element
  const sel = document.getElementById('langSelect');
  if (sel) sel.value = lang;
}

// Apply translations to all elements with data-i18n attribute
function applyTranslations(lang) {
  const t = translations[lang] || translations['en'];

  try {
    updateBackButtonLabel(lang);

    document.querySelectorAll('[data-i18n]').forEach(el => {
      const key = el.getAttribute('data-i18n');
      if (t[key] !== undefined && typeof t[key] === 'string') {
        // If the translation contains HTML (like <span>), use innerHTML
        if (t[key].indexOf('<') !== -1) {
          el.innerHTML = t[key];
        } else {
          el.textContent = t[key];
        }
      }
    });

    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
      const key = el.getAttribute('data-i18n-placeholder');
      if (t[key] !== undefined) {
        el.setAttribute('placeholder', t[key]);
      }
    });

    document.querySelectorAll('[data-i18n-aria-label]').forEach(el => {
      const key = el.getAttribute('data-i18n-aria-label');
      if (t[key] !== undefined) {
        el.setAttribute('aria-label', t[key]);
      }
    });

    document.documentElement.lang = lang;

    // Update product cards if on products page
    if (typeof updateProductCards === 'function') {
      updateProductCards();
    }
  } catch (e) {
    console.error('applyTranslations error:', e);
  }

  // Broadcast language change — fire-and-forget, always reached
  try {
    document.dispatchEvent(new CustomEvent('langChanged', { detail: { lang } }));
  } catch (e) {
    console.error('langChanged dispatch error:', e);
  }
}

function updateBackButtonLabel(lang) {
  const t = translations[lang] || translations['en'];
  const label = t.nav_back || translations.en.nav_back || 'Back';
  document.querySelectorAll('.subpage-back-btn').forEach(btn => {
    btn.setAttribute('aria-label', label);
    const text = btn.querySelector('[data-i18n="nav_back"]') || btn.querySelector('span:last-child');
    if (text) text.textContent = label;
  });
}

// Initialize i18n on page load
document.addEventListener('DOMContentLoaded', () => {
  const lang = getCurrentLang();
  const sel = document.getElementById('langSelect');
  if (sel) sel.value = lang;
  updateBackButtonLabel(lang);
  applyTranslations(lang);

  // Listen for language change
  if (sel) {
    sel.addEventListener('change', function() {
      setLanguage(this.value);
    });
  }
});
